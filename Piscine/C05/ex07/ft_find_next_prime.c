int	ft_find_next_prime(int nb)
{
	int	a;
	int	b;
	int	c;

	a = 1;
	if ((nb == 0 || nb == 1) || (nb < 0))
		return (2);
	if (nb < 100)
		c = nb;
	else
		c = nb / 2;
	while (++a < c)
	{
		b = nb % a;
		if (b == 0)
		{
		++nb;
		a = 1;
		}
	}
	return (nb);
}